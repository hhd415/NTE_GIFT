/* ============================================================
   2. 建模 + 校验
   ============================================================ */
function buildModel(raw) {
    return raw.map(r => {
        const ok =
            Number.isFinite(r.c100) &&
            Number.isFinite(r.c200) &&
            Number.isFinite(r.c400) &&
            r.c100 > 0 && r.c200 > 0 && r.c400 > 0;
        if (!ok) {
            console.warn("非法数据：", r);
            return null;
        }
        return {
            ...r,
            e200: Math.ceil(r.c200 - 2 * r.c100),
            e400: Math.ceil((r.c400 - 4 * r.c100) / 3)
        };
    }).filter(Boolean);
}

